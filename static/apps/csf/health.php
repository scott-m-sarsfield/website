		<h3>Exercise</h3><p>
You shouldn't be surprised that this one is on the list. It is unavoidable. Physical activity is essential to healthy living. The body was meant to move, and when it does not, it can become unhappy and ill. Physical activity stimulates the body's natural maintenance and repair systems that keep it going. It improves circulation to our heart and lungs. It gives us strength to stave off injuries, and it increases the mobility in our muscles and joints. Physical activity also releases endorphins; the feel good hormones that create a sense of general well being. Physical activity is good for the body and the mind.
Exercises include brisk walking, cycling, dancing, swimming, rowing, elliptical workouts and jogging. Yoga, and pilates are also good exercise workouts; however, they should be performed in conjunction with the cardiovascular-type workouts mentioned above.</p>
<h3>Eating healthy</h3><p>

Have you ever heard of the saying "you are what you eat" or "garbage in garbage out"? Well, it is true. What you put into your body directly affects how you feel physically, your mood, your mental clarity, your internal workings, and even your skin. Eating healthy does not mean eating expensive foods with little taste. As a matter of fact, there are some fantastic health recipes online and in cookbooks that are very healthy. Basically, you want to aim for a diet that is low in salt, fat and unprocessed foods and is high in fruits, vegetables, whole grains and omega-3 fatty acids. It is also good to take a multi vitamin to ensure you are meeting your nutrient requirements.</p>
<h3>Reduce stress</h3></p>
We have got to get rid of all of this stress. Stress happens when your life becomes out of balance physically, mentally or emotionally. This imbalance can be caused by internal stress like worrying too much, environmental stress like pressure from work, family or friends, or by stress from being fatigued or overworked. Being stressed out has the potential to affect your health in a variety of ways. You can become tired, sick, tense, irritable, and unable to think clearly. If you want to live a healthy lifestyle, you will need to manage the stress in your life so that it does not overtake you. This means taking charge of your thoughts, emotions, tasks, and environment to get your body back in balance.</p>	
